let color = prompt('Ingresa un color primario')

switch (color) {
  case 'rojo':
    alert('El color es rojo');
    break;
  case 'azul':
    alert('El color es azul');
    break;
  case 'amarillo':
    alert('El color es amarillo');
    break;
  default:
    alert('No es un color primario');
}
